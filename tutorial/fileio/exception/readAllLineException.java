package com.tutorial.fileio.exception;

public class readAllLineException extends RuntimeException {

	private static final long serialVersionUID = 1693157370652351189L;

	public readAllLineException() {
		super();
	}

	public readAllLineException(String message, Throwable cause) {
		super(message, cause);
	}

	public readAllLineException(String message) {
		super(message);
	}

	public readAllLineException(Throwable cause) {
		super(cause);
	}

}
